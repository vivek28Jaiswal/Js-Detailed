let weeks = "Thursday";

switch (weeks) {
    case "Sunday":
        console.log("Chill day");
        break;
    case "Monday":
        console.log("File Submission");
        break;
    case "Tuesday":
        console.log("Heavy Rain");
        break;
    case "Wednesday":
        console.log("Movie Plan with Friends");
        break;
    case "Thursday":
        console.log("Office Work");
        break;
    case "Friday":
        console.log("College Exam");
        break;
    case "Saturday":
        console.log("Week off");
        break;

    default:
        console.log("default case");
        break;
}